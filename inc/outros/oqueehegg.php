    <p align="justify">Antes do <b>Eggdrop</b>, devemos explicar para os leigos 
    o que é um <b>Bot</b>.<br>
    <br>
    Bot (vem de roBOT - robô, em inglês) é um programa que tem a função de 
    permanecer em algum canal do IRC e executar funções definidas por seu dono, 
    como proteger contra ataques, expulsar usuários mal comportados (que falam 
    palavrões, spam ou flood), oferecer jogos de pergunta-resposta, servir 
    arquivos, servir status OP para determinados usuários, e muitas outras 
    funções.</p>
    <p align="justify">Existem muitos tipos de Bots, como AcidBlood, ComBot, 
    EnergyMech, IncuBot, HackBot e outros. A grande maioria deles é antiga e sem 
    suporte, deixando apenas um tipo de bot que sobreviveu ao teste do tempo: o
    <b>EggDrop</b>.</p>
    <p align="justify">O Eggdrop (<a target="_blank" href="http://www.egghelp.org/">http://www.egghelp.org</a>) 
    foi criado por Robey Pointer por volta de Dezembro de 1993 para ajudar a 
    parar uma incessante guerra no canal #gayteens. É o Bot mais utilizado e 
    conhecido no mundo, e foi derivado de um outro bot que estava sendo escrito 
    na época chamado &quot;Unrest&quot;.<br>
    <br>
    Se você nunca usou um bot, você vai achar que o eggdrop permite um vasto 
    campo de opções para controle de canal, que podem ser expandidos para prover 
    ainda mais funções. O Eggdrop é programado em linguagem C e possui o código 
    fonte aberto, ou seja, qualquer programador pode modificar seu código e 
    desenvolver uma versão própria.<br>
    <br>
    Mas não é necessário ser programador para personalizar o seu Eggdrop, para 
    facilitar a inclusão de recursos e funções criaram os arquivos TCL, que são 
    scripts prontos para serem adicionados ao eggdrop. Existem centenas ou 
    milhares de TCLs disponíveis pela Internet, que adicionam recursos as vezes 
    surpreendentes.<br>
    <br>
    O Eggdrop foi criado para funcionar em sistemas operacionais Unix, mas 
    existe versão para Windows também, conhecida como <b>Windrop</b> (<a target="_blank" href="http://windrop.sourceforge.net">http://windrop.sourceforge.net</a>).<br>
    <br>
    Outro bot para Windows, que nada tem a ver com o Eggdrop mas que é também 
    excelente e possui janelas de configuração visual (ao contrário do Eggdrop 
    que tudo é configurado através de arquivos texto) chama-se <b>Winbot</b> (<a target="_blank" href="http://www.winbot.co.uk">http://www.winbot.co.uk</a>).</td>
