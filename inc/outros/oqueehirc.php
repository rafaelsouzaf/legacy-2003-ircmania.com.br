    <p align="justify">Para acessar o IRC, o usuário conecta- se a um <b>
    servidor</b> de <b>IRC</b>. Um <b>servidor </b>é o seu meio de acesso a uma 
    rede dentro do qual os usuários podem se comunicar. Então uma rede de IRC 
    são diversos servidores unidos entre si.<br>
    <br>
    Em seguida, a pessoa escolhe um <b>canal </b>(sala do chat) e dentro deste
    <b>canal</b> inicia o bate-papo em tempo real com outros usuários.<br>
    <br>
    Uma vez dentro do <b>canal,</b> o usuário pode manter conversas públicas ou 
    privadas com outros usuários. Numa tela principal teclam todas as pessoas 
    presentes, enviando recados públicos, que, portanto, podem ser lidos por 
    todos os participantes desse canal. Em paralelo a esse procedimento, você 
    pode <b>clicar</b> nos <b>nicks</b> das pessoas presentes para ter com elas 
    uma conversa particular. Cada usuário pode participar de quantos canais 
    quiser simultaneamente, bem como falar com tantas pessoas quanto desejar.<br>
    <br>
    Cada <b>canal</b> possui na maior parte das vezes um ou vários <b>operadores</b> 
    (identificadas pela presença do símbolo arroba (@) na frente do seu 
    apelido), que é a pessoa que define as regras do canal. Essas regras se 
    referem a vários tipos de comportamento: a etiqueta, o linguajar, brigas 
    entre usuários, impedimento de propagandas, etc. Somente eles tem meios de 
    excluir pessoas que infringirem as regras estabelecidas no canal.</p>
