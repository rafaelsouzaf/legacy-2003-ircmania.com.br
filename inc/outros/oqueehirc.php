    <p align="justify">Primeiro é bom saber o que é <b>&quot;Chat&quot;;</b> vem do inglês 
    e significa Bate-papo, conversar, fofocar. Um <b>chat</b>, em jargão 
    internauta, significa um lugar virtual onde diversas pessoas podem se 
    encontrar e conversar (teclando) sobre os mais variados temas.<br>
    <br>
    A conversa acontece praticamente em tempo real, ou seja: alguém digita uma 
    mensagem e a envia através do computador. Em uma seqüência imediata outra 
    pessoa recebe a mensagem do ‘outro lado’ podendo ler e responder a mensagem 
    , dentro da mesma continuidade.<br>
    <br>
    Na Internet, este tipo de contato pode ser feito de várias maneiras, sendo 
    que as três principais são o <b>Webchat</b> (como o chat do
    <a target="_blank" href="http://batepapo.uol.com.br">UOL</a> ou
    <a target="_blank" href="http://chat.terra.com.br">Terra</a>) , o <b>ICQ</b> 
    (<a target="_blank" href="http://www.icq.com">www.icq.com</a>) e o <b>IRC</b> 
    (Internet Relay Chat), sendo este ultimo aquele sobre o qual nos deteremos.</p>
    <ul>
      <li>
      <p align="justify"><strong>I R C</strong></li>
    </ul>
    <p align="justify">O IRC (Internet Relay Chat) é um chat mais avançado, com 
    mais opções, mais rápido e mais utilizado do que o WebChat. Para a 
    utilização do <b>IRC</b>, o usuário precisará de um programa específico (o 
    mais usado chama-se <a href="http://www.mirc.com" target="_blank">mIRC</a>) 
    ou pode também acessar pela web sem a necessidade de nenhuma instalação, 
    através do que chamamos de <b>WebIRC</b>.<br>
    <br>
    Existem no Brasil e em Portugal várias redes de IRC, tendo nelas dezenas de 
    milhares de salas de chat (canais). Grande parte das redes de IRC são 
    criadas e sustentadas pelos próprios usuários, na maior parte das vezes sem 
    qualquer retorno financeiro.</p>
