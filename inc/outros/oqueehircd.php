    <p align="justify"><b>&quot;I</b>nternet <b>R</b>elay <b>C</b>hat <b>D</b>aemon&quot;. 
    O IRCD é um programa que serve como base para a criação de um servidor de 
    IRC. É o IRCd que recebe as conexões dos usuários e liga tudo através dos 
    canais e dos serviços disponíveis no IRC.<br>
    <br>
    Uma rede de IRC nada mais é do que vários servidores (utilizando programas 
    IRCd) ligados entre si.<br>
    <br>
    Quase todos os IRCds desenvolvidos são para sistemas operacional Unix, como 
    Linux ou FreeBSD. Existem IRCds para Windows também, mas nesses casos são 
    chamados de <u>wIRCd</u> (com um 'w' para diferenciar). Através de wIRCds 
    qualquer pessoa pode criar em seu computador pessoal um servidor de IRC 
    próprio.<br>
    <br>
    O primeiro IRCd da história foi criado pelo finlandês Jarkko Oikarinen e 
    lançado no dia 26 de Agosto de 1988, quando ocorreu a criação do protocolo 
    IRC.<br>
    <br>
    Mas uma rede de IRC não é formada apenas pelo IRCd, os Services também tem 
    uma função de vital importância.<br>
    <br>
    Os Services (ou wServices para Windows) são programas que ligados ao IRCd 
    possuem o poder de oferecer serviços como registro de nicks e canais. São os 
    Services os responsáveis pelos famosos NickServ, Chanserv e Memoserv.<br>
    <br>
    Para baixar os programas visite nossas seções de w(IRCd) e (w)Service. Veja 
    menu ao lado.</td>
