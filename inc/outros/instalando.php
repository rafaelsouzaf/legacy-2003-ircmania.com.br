    <td width="100%">
    <p align="justify">O melhor método para acessar o IRC é instalando um 
    programa criado especificamente para isso e o
    <a target="_blank" href="http://www.mirc.com">mIRC</a> é o mais utilizado no 
    mundo.<br>
    <br>
    Não se assuste com o número de etapas descritas abaixo, a maior parte das 
    pessoas saberia pular várias delas, mas como partimos do princípio de que 
    quem está lendo esta página é iniciante em IRC e talvez em Internet, 
    escolhemos mostrar a instalação e o acesso do modo mais completo e simples 
    possível.<br><br>
    Após conectar a primeira vez você só precisará repetir as duas ultimas 
    etapas, 8 e 9.</p>
    <hr color="#000000" width="80%" size="1">
    <p align="justify"><b>1)</b> O primeiro passo é fazer o download do mIRC. O 
    link a seguir leva direto a seção de download do site oficial:
    <a href="http://www.mirc.com/get.html">www.mirc.com/get.html</a></p>
    <p align="center"><a href="imagens/outros/instalando/instalando1a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando1b.gif" width="133" height="114"></a></p>
    <p align="justify">A página de Download (<i>figura acima</i>) mostra vários 
    países onde o programa está disponível. Como todas as versões dos programas 
    são iguais, escolha qualquer um dos países e inicie o download.<br>
    <br><hr color="#C0C0C0" width="80%" size="1">
    <br>
    <b>2)</b> Após ter baixado o programa dê dois clicks e vamos iniciar a 
    instalação (figura abaixo).</p>
    <p align="center"><a href="imagens/outros/instalando/instalando2a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando2b.gif" width="133" height="130"></a></p>
    <p align="justify">Clique no botão &quot;<u><b>Next</b></u>&quot;.<br>
    <br><hr color="#C0C0C0" width="80%" size="1">
    <br>
    <b>3)</b> A Figura abaixo mostra os termos de utilização do programa. O mIRC 
    é <i>shareware</i>, então o registro (que custa alguns $) deve ser feito após 30 
    dias de utilização. Mas podemos dizer que o registro passa a ser opcional já 
    que é possível utilizar o programa por tempo indeterminado.</p>
    <p align="center"><a href="imagens/outros/instalando/instalando3a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando3b.gif" width="133" height="130"></a></p>
    <p align="justify">Clique em <u><b>YES</b></u> e prossigamos.<br>
    <br><hr color="#C0C0C0" width="80%" size="1">
    <br>
    <b>4)</b> A etapa quatro é onde escolhemos em que lugar queremos instalar o 
    mIRC. Normalmente ele já vem com o caminho &quot;<b>C:\mIRC</b>&quot; definido (<i>ver 
    figura abaixo</i>). Caso queira mudar fique a vontade.</p>
    <p align="center"><a href="imagens/outros/instalando/instalando4a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando4b.gif" width="133" height="130"></a></p>
    <p align="justify">Clique em &quot;<u><b>INSTALL</b></u>&quot; e o mIRC será 
    instalado.<br>
    <br><hr color="#C0C0C0" width="80%" size="1">
    <br>
    <b>5)</b> Após instalado ele mostra a janelinha abaixo, onde podemos 
    escolher se desejamos iniciar o mIRC agora ou finalizar a instalação.</p>
    <p align="center"><a href="imagens/outros/instalando/instalando5a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando5b.gif" width="133" height="130"></a></p>
    <p align="justify">Vamos finalizar a instalação sem abrir o mIRC, desmarque 
    as opções e clique em <b>&quot;<u>Finish</u>&quot;</b>.<br>
    <br><hr color="#C0C0C0" width="80%" size="1">
    <br>
    <b>6) Configurando.</b> Podemos abrir o mIRC clicando no ícone que está em 
    nossa área de trabalho ou no menu Iniciar. Faça isso e irá aparecer a 
    janelinha abaixo.</p>
    <p align="center"><a href="imagens/outros/instalando/instalando6a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando6b.gif" width="133" height="110"></a></p>
    <p align="justify">Como a versão do nosso mIRC não está registrada e no 
    momento não queremos registrar, basta clicar em <b>&quot;<u>Continue</u>&quot;</b>.<br>
    <br><hr color="#C0C0C0" width="80%" size="1">
    <br>
    <b>7)</b> Opa, estamos acabando. A janela a seguir é onde você deve digitar 
    seus dados, que serão visíveis para os outros usuários do IRC. Devemos 
    deixar claro que não é necessário digitar informações reais, coloque o que 
    quiser.</p>
    <p align="center"><a href="imagens/outros/instalando/instalando7a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando7b.gif" width="133" height="110"></a></p>
    <p align="justify">Preencha os campos <i>Full Name</i> (nome completo), <i>
    Email Address</i> (seu email), <i>Nickname</i> (seu apelido) e <i>
    Alternative</i> (seu apelido alternativo caso o primeiro esteja sendo 
    utilizado). Após preencher esses campos clique em <u><b>OK</b></u>.<br>
    <br><hr color="#C0C0C0" width="80%" size="1">
    <br>
    <b>8)</b> Bem, a instalação já foi feita, agora precisamos decidir em 
    qualquer rede de IRC queremos conectar. Existem redes nacionais e 
    internacionais, algumas delas com mais de 100.000 usuários simultâneos. Para 
    ver a relação de redes em língua portuguesa,
    <a target="_blank" href="redes.php">clique aqui</a>.<br>
    <br>
    Visitando o link acima você verá que todos os endereços das redes estão no 
    formato<b> irc.rede.org</b> , <b>irc.rede.net</b> ou <b>irc.rede.com.br </b>
    e assim por diante.<br>
    <br>
    Após escolher a rede (ou servidor) que quer acessar, basta digitar na janela 
    do mIRC o comando <b>/server irc.RedeEscolhida.com</b> (veja modelo na 
    figura abaixo).</p>
    <p align="center"><a href="imagens/outros/instalando/instalando8a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando8b.gif" width="133" height="110"></a></p>
    <p align="justify">Após digitar o comando <b>/server irc.suarede.com</b>, 
    pressionar <i>Enter</i> e aguardar alguns instantes, aparecerá mensagens 
    semelhantes a abaixo (<i>ver figura na etapa 9</i>). Ela significa que você 
    já está conectado ao IRC. Caso tenha ocorrido algum problema, escolha outra 
    rede e refaça a etapa 8.<br>
    <br><hr color="#C0C0C0" width="80%" size="1">
    <br>
    <b>9)</b> Depois de conectado agora só falta entrar em alguma sala e começar 
    a conversar. O comando para ver a lista de salas disponíveis é o <b>/LIST</b>, 
    digite /LIST, pressione Enter e verá todas as salas da rede.<br>
    <br>
    Mas se preferir entrar direto em alguma sala utilize o comando <b>/JOIN 
    BRASIL</b> ou troque o BRASIL pela sala de sua preferência (veja imagem 
    abaixo).</p>
    <p align="center"><a href="imagens/outros/instalando/instalando9a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando9b.gif" width="133" height="110"></a></p>
    <p align="center"><a href="imagens/outros/instalando/instalando10a.gif" target="_blank">
    <img border="0" src="imagens/outros/instalando/instalando10b.gif" width="133" height="100"></a></p>
