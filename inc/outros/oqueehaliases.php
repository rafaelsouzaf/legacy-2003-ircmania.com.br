    <p align="justify"><i>Aliases</i> são frases prontas utilizadas 
    especialmente por usuários que possuem clientes de IRC (mIRC, xChat, BitchX, 
    etc) para acesso ao chat.<br>
    <br>
    Normalmente as <i>aliases</i> são coloridas e cheias de detalhes, e até 
    interativas como as que os usuários da sala de chat precisam marcar a frase 
    com o mouse para saber o que está escrito.<br>
    <br>
