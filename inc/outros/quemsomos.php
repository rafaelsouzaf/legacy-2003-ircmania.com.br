    <p align="justify"></u>Os sites IRCmania e ChatMania são portais de busca e 
    notícias relacionadas aos meios de comunicação on-line, principalmente IRC.</p>
    <p align="justify">Criado em Junho do ano 2000, o site então chamado vIRCio.org 
    englobava downloads, artigos e dicas sobre IRC além de uma enorme relação de 
    textos sobre comportamento on-line, servindo inúmeras vezes de referência 
    para reportagens na imprensa nacional.</p>
    <p align="justify">Em Abril de 2004, após mais de 1 ano de desenvolvimento, 
    foram ao ar as versões atuais dos sites tendo design profissional e código 
    próprio.<br>
