    <td width="100%">
    <p align="justify"><b>Scripts</b> são modificações do mIRC (ou qualquer 
    outro cliente de IRC) criada por usuários e que adicionam novos recursos ao 
    programa.<br>
    <br>
    Conseqüentemente <b>Scripting</b> é a linguagem de programação ou de regras 
    que tornam viável alterar parte da estrutura dos clientes de IRC. Mudar 
    cores, sons, letras, imagens, implementar novos comandos, estatísticas e 
    muitas outras coisas.<br>
    <br>
    Tudo depende do grau de criatividade, conhecimento e originalidade do 
    Scripter (o sujeito que faz scripting).<br>
    <br>
    Mas para modificar o cliente de IRC não é obrigação saber Scripting. Existem 
    os arquivos chamados Addons que são complementos prontos para instalar e que 
    também adicionam recursos ao cliente de IRC. São úteis quando você quer 
    acrescentar apenas uma característica de um script, e não todas as frescuras 
    que grande parte dos Scripts possuem.<br>
    <br>
    Apesar de ser o mais utilizado, o mIRC Scripting é uma das linguagens mais 
    pobres se comparadas a outros clientes de IRC.<br>
    <br>
    Existem concorrentes do mIRC que permitem acrescentar programação Javascript, 
    Perl e até mesmo PHP em sua linguagem de scripting, mas como para tudo na 
    Internet, vale aquela famosa frase: &quot;nem sempre o mais utilizado é o 
    melhor.&quot;</td>
